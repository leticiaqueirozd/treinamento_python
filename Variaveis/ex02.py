segundos = float(input("Digite um tempo em segundos: "))

dias = float(segundos / 86400)
horas = float(segundos / 3600)
minutos = float(segundos / 60)

print("A quantidade de segundos em dias é: ", dias)
print("A quantidade de horas em horas é: ", horas)
print("A quantidade de minutos em dias é: ", minutos)